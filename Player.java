package cz.makub;

public class Player {
   public String age;
   public String birthDate;
   public String experience;
   public String height;
   public String name;
   public String prior;
   public String weight;
   public String picUrl;
   
   public Player(String age, String birthDate, String experience, 
         String height, String name, String prior, 
         String weight, String picUrl) {
      this.age = age;
      this.birthDate = birthDate;
      this.experience = experience;
      this.height = height;
      this.name = name;
      this.prior = prior;
      this.weight = weight;
      this.picUrl = picUrl;
   }
}
